package com.uplooking.pojo;

/*
 *  用户注册实体
 */
public class PortalUserInfo {

	private Integer portalId;
	private String portalName;
	private String portalPwd;
	private String portalAddr;
	private String portalSex;
	private String portalTime;
	private String portalIntroduce;

	public Integer getPortalId() {
		return portalId;
	}

	public void setPortalId(Integer portalId) {
		this.portalId = portalId;
	}

	public String getPortalName() {
		return portalName;
	}

	public void setPortalName(String portalName) {
		this.portalName = portalName;
	}

	public String getPortalPwd() {
		return portalPwd;
	}

	public void setPortalPwd(String portalPwd) {
		this.portalPwd = portalPwd;
	}

	public String getPortalAddr() {
		return portalAddr;
	}

	public void setPortalAddr(String portalAddr) {
		this.portalAddr = portalAddr;
	}

	public String getPortalSex() {
		return portalSex;
	}

	public void setPortalSex(String portalSex) {
		this.portalSex = portalSex;
	}

	public String getPortalTime() {
		return portalTime;
	}

	public void setPortalTime(String portalTime) {
		this.portalTime = portalTime;
	}

	public String getPortalIntroduce() {
		return portalIntroduce;
	}

	public void setPortalIntroduce(String portalIntroduce) {
		this.portalIntroduce = portalIntroduce;
	}

}
